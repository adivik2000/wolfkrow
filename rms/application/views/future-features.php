	<h1>Future Features</h1>
	
	<h2>Analytical Tools for Admin Dashboard</h2>
	
	<p>The database is currently storing a wealth of information that is not
		readily accessible to the manager: The time it takes a vendor to 
		complete an order once it is active. The length of time it takes a 
		guest to complete a meal. Revenue over a period of months. Most used 
		vendors. Most frequent guests. These could be added in as desired
		by the manager.</p>
		
	<h2>Availability Status of Vendors</h2>
	
	<p>Currently, all vendors are listed for the guests to choose from. 
		Obviously, in the real world, all of these vendors would not be 
		available all of the time. We could create an availability status 
		that vendors could control. It could even be automatically changed
		based on the number of active orders a vendor currently has up. </p>
		
	<h2>Additional Services</h2>
	
	<p>Additional services available for vendor types are currently 
		show on the select a vendor page, but these options are disabled. 
		Once enabled these options would allow guests to add
		services to their order beyond the standard package. Down the road,
		prices for each of these services could be configurable by vendors. 
		Which additional services are available could also vary by vendor.</p>
		
	<h2>Credit Card Account</h2>
	
	<p>Currently, credit card info is disabled for the prototype. More
		work would need to be put into this system to allow for the free 
		interchange of money between vendors, guests, and the manager as is
		simulated in this prototype. The goal would be an iTunes-ish style of
		account deductions.</p>
		
