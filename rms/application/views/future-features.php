	<p>This would allow the manager to view the meal status of individual 
		guests. The manager would also be able to see the vendors, their
		ratings, and be able to deactivate vendor accounts as needed.</p>
		
	<h2>Vendor Reviews</h2>
	
	<p>Beyond ratings, guests would be allowed to write reviews on vendors
		they have used.</p>
	
